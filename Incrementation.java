public class Incrementation {
    public static void main(String[] args) {
        int n;
        n=10;
        System.out.println("n\n"+n++);
        System.out.println(n);
        System.out.println(++n);
        int a=5;
        System.out.println("a\n"+(a+=1));
        System.out.println((a-=1));
        
    }
    
}
